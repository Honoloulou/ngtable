function randomDateArr(){
	var oneday = 1000*60*60*24,
			todayms = (new Date()).getTime(),
			first = todayms + oneday * getRandomInt(1,20),
			last = first + oneday * getRandomInt(30,120);

			first = new Date(first);
			last = new Date(last);

			first = first.getMonth() + '/' + first.getDate() + '/' + first.getFullYear();
			last = last.getMonth() + '/' + last.getDate() + '/' + last.getFullYear();
			
 	return [first,last];
}

function currency(input) {
            input = '' + input; // cast as a string, so it can use split()
            var n;
            if (!isNaN(input)) {
                n = input.split('.');
                n[0] = (parseInt(n[0])).toLocaleString();

                // add $ sign
                (n[0].indexOf('-') === 0) ?
                    // negative number : add $ after the minus
                    n[0] = n[0].replace('-','-$') : 
                    n[0] = '$' + n[0];

                // add .00 if it's an integer
                (n[1] != undefined) ? 
                    input = n[0] + '.' +  n[1] :
                    input = n[0] + '.00';
            }

            return input;
}

